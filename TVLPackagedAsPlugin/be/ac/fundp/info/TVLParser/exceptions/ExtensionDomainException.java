package be.ac.fundp.info.TVLParser.exceptions;

public class ExtensionDomainException extends Exception {
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 3800821054833037179L;

	public ExtensionDomainException() {
		super();
	}

	public ExtensionDomainException(String message) {
		super(message);
	}

	public ExtensionDomainException(Exception source) {
		super(source);
	}

}
